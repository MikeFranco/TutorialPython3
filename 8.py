
"""                                   Sentencia if
La sentencia if es una condicial, si pasa lo que haya "dentro" de esta sentencia (generalmente un operador logico)
se realizara lo que hay en la identacion debajo del if

Operadores logicos que soporta un if
Igual: a == b
No igual: a != b
Menor que: a < b
Menor o igual que: a <= b
Mayor que: a > b
Mayor o igual que: a >= b
"""
a = 100
b = 50
if a<b:
  print("Es es un error")
elif a>b:
  print("Esto es correcto")
